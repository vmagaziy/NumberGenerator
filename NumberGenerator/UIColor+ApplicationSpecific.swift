import UIKit

extension UIColor {
    class func brandDarkColor() -> UIColor {
        return UIColor(red: 0.6, green: 0.76, blue: 0.22, alpha: 1.0)
    }
    
    class func brandLightColor() -> UIColor {
        return UIColor.whiteColor()
    }    
}
